#include <unistd.h>

void	ft_is_negative(int n)
{
	char	u;
	char	d;

	u = 'P';
	d = 'N';
	if ( n>= 0 ){
		write( 1,&u,1 );
	}
	else {
		write( 1,&d,1 );
	}
}

